month = input("Enter a month:")
if "September" or "October" or "November":
    print(month,"The season is Autumn")
elif "December" or "January" or "February":
    print(month,"The season is Winter")
elif "March" or "April" or "May":
    print(month,"The season is Spring")
elif "June" or "July" or "August":
    print(month,"The season is Summer")
else:
    print("Invalid month please give correct month")